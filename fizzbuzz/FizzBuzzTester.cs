using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace fizzbuzz
{
    class FizzBuzzTester
    {
        public string CheckIfFizzAndOrBuzz(number)
        {
            if (number % 3 = 0 && number % 5 = 0)
            {
                return "FizzBuzz";
            }
            return "Buzz";
        }
    }
}
